def getCityNum(name):  # 输入地址获得行政区号
    name = name[:6]
    numList = []
    flag = 0
    for i in cityName:
        if i[:-1] in name and len(i[:-1]) >= 2:
            # print(i, cityNum[flag])
            numList.append(cityNum[flag])
        flag += 1
    numList = sorted(numList, reverse=True)

    firstDegree = []
    for i in numList:
        firstDegree.append(i[:2])
    firstDegree = list(set(firstDegree))
    answer = []
    for i in firstDegree:
        for j in numList:
            if i == j[:2]:
                answer.append(j)
                break

    return answer
